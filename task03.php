function display_array($array, $level = 1): void
{
    $color = match ($level) {
        1 => "red",
        2 => "blue",
        3 => "green",
        4 => "purple",
        default => "yellow",
    };
    echo "<ul>";
    foreach ($array as $key => $value) {
        echo "<li style='color: $color;'>$key</li>";
        if (is_array($value)) {
            display_array($value, $level + 1);
        } else {
            echo "<li style='color: $color;'>$value</li>";
        }
    }
    echo "</ul>";
}

